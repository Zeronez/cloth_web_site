def set_run_font(run, size=18, bold=False, color=RGBColor(255, 255, 255)):
    run.font.name = "Arial"
    run.font.size = PPt(size)
    run.font.bold = bold
    run.font.color.rgb = color


def clear_slide_text(slide):
    for shape in slide.shapes:
        if hasattr(shape, "text_frame"):
            shape.text_frame.clear()


def add_textbox(slide, left, top, width, height, text, size=22, bold=False, color=RGBColor(255, 255, 255), align=PP_ALIGN.LEFT):
    box = slide.shapes.add_textbox(left, top, width, height)
    tf = box.text_frame
    tf.word_wrap = True
    tf.margin_left = PCm(0.1)
    tf.margin_right = PCm(0.1)
    tf.margin_top = PCm(0.05)
    tf.margin_bottom = PCm(0.05)
    p = tf.paragraphs[0]
    p.alignment = align
    run = p.add_run()
    run.text = text
    set_run_font(run, size=size, bold=bold, color=color)
    return box


def add_bullets(slide, bullets, left, top, width, height, size=20):
    box = slide.shapes.add_textbox(left, top, width, height)
    tf = box.text_frame
    tf.word_wrap = True
    tf.margin_left = PCm(0.2)
    tf.margin_right = PCm(0.2)
    tf.margin_top = PCm(0.1)
    tf.margin_bottom = PCm(0.1)
    for idx, bullet in enumerate(bullets):
        p = tf.paragraphs[0] if idx == 0 else tf.add_paragraph()
        p.text = bullet
        p.level = 0
        p.space_after = PPt(8)
        p.font.name = "Arial"
        p.font.size = PPt(size)
        p.font.color.rgb = RGBColor(235, 245, 246)
    return box


def add_image_if_possible(slide, image_path, left, top, width=None, height=None):
    if not image_path or not image_path.exists() or image_path.suffix.lower() == ".svg":
        return
    slide.shapes.add_picture(str(image_path), left, top, width=width, height=height)


def build_docx():
    doc = Document()
    section = doc.sections[0]
    section.page_width = Cm(21)
    section.page_height = Cm(29.7)
    section.top_margin = Cm(2)
    section.bottom_margin = Cm(2)
    section.left_margin = Cm(2)
    section.right_margin = Cm(2)

    styles = doc.styles
    styles["Normal"].font.name = "Times New Roman"
    styles["Normal"].font.size = Pt(12)

    heading = doc.add_paragraph()
    heading.alignment = 1
    run = heading.add_run("Текст выступления к дипломному проекту")
    run.bold = True
    run.font.name = "Times New Roman"
    run.font.size = Pt(14)

    for page_index, paragraphs in enumerate(SPEECH_PAGES):
        if page_index:
            doc.add_paragraph().add_run().add_break(WD_BREAK.PAGE)
        for text in paragraphs:
            p = doc.add_paragraph()
            p.alignment = 3
            p.paragraph_format.first_line_indent = Cm(1.25)
            p.paragraph_format.line_spacing_rule = WD_LINE_SPACING.ONE_POINT_FIVE
            p.paragraph_format.space_after = Pt(0)
            run = p.add_run(text)
            run.font.name = "Times New Roman"
            run.font.size = Pt(12)

    doc.save(OUT_DOCX)


def build_pptx():
    prs = Presentation(str(TEMPLATE))
    for index, slide in enumerate(prs.slides):
        data = SLIDES[index] if index < len(SLIDES) else {}
        clear_slide_text(slide)

        if index in {0, 4, 11, 13, 16}:
            add_textbox(slide, PCm(1.2), PCm(1.2), PCm(22), PCm(1.5), data.get("title", ""), size=30, bold=True)
            if data.get("subtitle"):
                add_textbox(slide, PCm(1.3), PCm(4.6), PCm(17), PCm(2.4), data["subtitle"], size=17)
            if data.get("image"):
                add_image_if_possible(slide, data["image"], PCm(20.7), PCm(1.0), width=PCm(4.5))
            continue

        add_textbox(slide, PCm(1.0), PCm(0.75), PCm(21.5), PCm(1.1), data.get("title", ""), size=26, bold=True)
        add_textbox(slide, PCm(24.0), PCm(0.8), PCm(1.0), PCm(0.7), str(index + 1), size=13, color=RGBColor(210, 220, 225), align=PP_ALIGN.RIGHT)

        if data.get("lead"):
            add_textbox(slide, PCm(1.2), PCm(2.0), PCm(22), PCm(1.0), data["lead"], size=18, bold=True, color=RGBColor(237, 37, 78))
            bullets_top = PCm(3.25)
            bullets_height = PCm(8.0)
        else:
            bullets_top = PCm(2.35)
            bullets_height = PCm(9.0)

        if data.get("image"):
            add_bullets(slide, data.get("bullets", []), PCm(1.1), bullets_top, PCm(13.0), bullets_height, size=18)
            add_image_if_possible(slide, data["image"], PCm(15.2), PCm(2.1), width=PCm(9.5))
        else:
            add_bullets(slide, data.get("bullets", []), PCm(1.2), bullets_top, PCm(22.5), bullets_height, size=20)

    prs.save(OUT_PPTX)


if __name__ == "__main__":
    build_docx()
    build_pptx()
    print(OUT_DOCX)
    print(OUT_PPTX)
