from docx import Document
from docx.oxml import OxmlElement
from docx.text.paragraph import Paragraph


def delete_paragraph(paragraph: Paragraph) -> None:
    element = paragraph._element
    element.getparent().remove(element)


def insert_paragraph_after(paragraph: Paragraph, text: str, *, style: str) -> Paragraph:
    new_p = OxmlElement("w:p")
    paragraph._p.addnext(new_p)
    new_par = Paragraph(new_p, paragraph._parent)
    new_par.style = style
    new_par.add_run(text)
    return new_par


def find_heading_index(doc: Document, *, level: int, text: str) -> int:
    style = f"Heading {level}"
    for i, p in enumerate(doc.paragraphs):
        if p.style.name == style and p.text.strip() == text:
            return i
    raise ValueError(f"Heading {level} not found: {text!r}")


def find_heading2_index_in_range(
    doc: Document, *, title: str, start_i: int, end_i: int
) -> int:
    for i in range(start_i, end_i):
        p = doc.paragraphs[i]
        if p.style.name == "Heading 2" and p.text.strip() == title:
            return i
    raise ValueError(f"Heading 2 not found in chapter range: {title!r}")


def clear_section_body(doc: Document, *, start_i: int, end_i: int) -> int:
    i = start_i
    while i < end_i:
        p = doc.paragraphs[i]
        if p.style.name in ("Heading 1", "Heading 2"):
            break
        delete_paragraph(p)
        end_i -= 1
    return end_i


def write_section(doc: Document, *, h2_index: int, lines: list[str]) -> None:
    anchor = doc.paragraphs[h2_index]
    prev = anchor
    for line in lines:
        if line.startswith("- "):
            prev = insert_paragraph_after(prev, line[2:], style="List Paragraph")
        else:
            prev = insert_paragraph_after(prev, line, style="Normal")


