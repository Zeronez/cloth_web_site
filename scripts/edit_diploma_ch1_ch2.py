def replace_chapter(doc: Document, *, h1_start: str, h1_end: str, titles: list[str]) -> None:
    for title in titles:
        start_i = find_heading_index(doc, level=1, text=h1_start)
        end_i = find_heading_index(doc, level=1, text=h1_end)
        h2_index = find_heading2_index_in_range(
            doc, title=title, start_i=start_i + 1, end_i=end_i
        )
        end_i = clear_section_body(doc, start_i=h2_index + 1, end_i=end_i)
        write_section(doc, h2_index=h2_index, lines=CONTENT[title])


